package application;

class Clothes
{
	Integer id;
	String name;
	String kind;
	String part;
	Double index;
	
	Clothes()
	{
		
	}
	
	Clothes(Integer id, String name, String kind, String part, Double index)
	{
		this.id = id;
		this.name = name;
		this.kind = kind;
		this.part = part;
		this.index = index;
	}
}